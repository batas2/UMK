void MyWidget::Line(uchar *bits, QPoint s, QPoint t, uchar R, uchar G, uchar B) {
    if (s.x() == t.x()) {
        int yMin = s.y() < t.y() ? s.y() : t.y();
        int yMax = s.y() < t.y() ? t.y() : s.y();

        for (int y = yMin; y < yMax; y++) {
            SetPixel(bits, s.x(), y, R, G, B);
        }
    } else {
        double a = (t.y() - s.y()) / (double) (t.x() - s.x());
        double b = -a * s.x() + s.y();

        if (a >= -1 && a <= 1) {
            int xMin = s.x() < t.x() ? s.x() : t.x();
            int xMax = s.x() < t.x() ? t.x() : s.x();

            double y = a * xMin + b;
            for (int x = xMin; x < xMax; x++) {
                SetPixel(bits, x, y, R, G, B);
                y += a;
            }
        } else {
            int yMin = s.y() < t.y() ? s.y() : t.y();
            int yMax = s.y() < t.y() ? t.y() : s.y();

            double x = (yMin - b) / a;
            double a1 = 1 / a;
            for (int y = yMin; y < yMax; y++) {
                SetPixel(bits, x, y, R, G, B);
                x += a1;
            }
        }
    }
}

void MyWidget::keyPressEvent(QKeyEvent * e) {
    //std::cout << e->key() << std::endl;
    int s = 15;
    int k = 1;
    double q = 0.1;

    switch (e->key()) {
        case 81: //Q
            if (w == 1) {
                w = -1;
            } else {
                w = 1;
            }
            break;
        case 82: //R
            D = 500;

            ScX = 8;
            ScY = 8;
            ScZ = 8;

            AlfaX = 0;
            AlfaY = 0;
            AlfaZ = 0;

            Tx = 0;
            Ty = 0;
            Tz = 0;

            break;
        case 87:
            D += w * s; //W
            _POV(Z) = D;
            _POV.norm();
            break;
        case 69: //E - Przelaczanie pomiedzy widokami
            if (_func_index == 0)
                _func_index = 1;
            else
                _func_index = 0;
            break;
        case 65: ScX += w * q; //A
            break;
        case 83: ScY += w * q; //s
            break;
        case 68: ScZ += w * q; //d
            break;
        case 90: AlfaX += w * q; //z
            break;
        case 88: AlfaY += w * q; //x
            break;
        case 67: AlfaZ += w * q; //c
            break;
        case 73: Tx += w * s; //i
            break;
        case 79: Ty += w * s; //o
            break;
        case 80: Tz += w * s; //p
            break;
        case 84:
            _S += w * k;
            if (_S % 5 == 0) {
                _S += w;
            }
            if (_S < 1)
                _S = 1;
            if (_S > 7)
                _S = 7;
            break;
    }
    repaint();
}

